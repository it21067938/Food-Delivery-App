import mongoose from "mongoose";

const UserShema = new mongoose.Schema({
  uid: {
    type: String,
    required: true,
  },
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const user = mongoose.model('User', UserShema);
export default user; 
